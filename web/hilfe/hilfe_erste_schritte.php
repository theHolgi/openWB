<h3>Bedienung</h3>
<h4> Lademodus</h4>
<p>
	Der Unterschied zwischen "Standby" und "Stop" ist, dass bei "Stop" keine Ladung erlaubt ist.<br>
	Im Standby  Modus kann die Ladung durch Nachtladen und Zielladen aktiviert werden.
</p>
<hr>
<h4>Ladeeinstellung</h4>
<h5>Meldung "Lastmanagement aktiv, Ladeleistung reduziert"</h5>
<p>
	Wurde innerhalb der letzten 2 Minuten das Lastmanagement aktiv, wird diese Meldung auf der Hauptseite unterhalb des Graphen angezeigt.<br>
	Sie dient rein der Information, dass das Lastmanagement von openWB aktiv ist und die gewünschte bzw. eingestellte Ladeleistung derzeit nicht möglich ist.<br>
	Es besteht kein Handlungsbedarf. Wenn mehr Leistung zur Verfügung steht, regelt openWB automatisch auf den gewünschten Wert hoch.
</p>
